public class Weapon{
    private int toHit;
    private int saveMod;
    private int attacks;
    private int range;

    public Weapon(int toHit, int saveMod, int attacks, int range){
        this.toHit=toHit;
        this.saveMod=saveMod;
        this.attacks=attacks;
        this.range=range;
    }
}